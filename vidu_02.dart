/*
  - khai bao bien bang tu khoa var
  - khai bao bien bang kieu du lieu cu the (String, int, double, bool)
  - khai bao bang object
*/

void main() {
  // khai bao bien bang tu khoa var
  var ten = "Sanh";
  var tuoi = 18;

  // khai bao bien bang kieu du lieu cu the
  String name = "Sanh";
  int age = 18;

  // moi thu trong flutter deu la object
  // khai bao bang object
  Object tenNguoiDung = "Sanh";
  print("Ten nguoi dung: $tenNguoiDung");
}
